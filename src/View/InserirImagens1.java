    // Função que recebe uma imagem do tipo Buffered e calcula a quantidade de pixels
    private static int[] pegaPixels(BufferedImage imagem) {
        final int width = imagem.getWidth(componente);
        final int height = imagem.getHeight(componente);
        int size = width * height;

        PixelGrabber pixel = new PixelGrabber(imagem, 0, 0, width, height, new int[size], 0, width);
        pixel.startGrabbing();
        return (int[]) pixel.getPixels();
    }

