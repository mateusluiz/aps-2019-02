    private void btnBuscar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscar1MouseClicked

        // Abrir um seletor de arquivos
        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Procurar imagem");
        file.setFileSelectionMode(JFileChooser.FILES_ONLY);

        //Filtro para aparecer somente imagens
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagem", "jpg", "png", "jpeg");

        file.setFileFilter(filtro);
        int retorno = file.showOpenDialog(this);

        // Recebe caminho da imagem
        File arquivo = file.getSelectedFile();

        String caminho = arquivo.getPath();

        if (retorno == JFileChooser.APPROVE_OPTION) {
            // Insere caminho no JText
            txtImagem1.setText(caminho);

            // Insere imagem na label
            lblImagem1.setIcon(new ImageIcon(new ImageIcon(arquivo.getPath()).getImage().getScaledInstance(284, 135, Image.SCALE_DEFAULT)));
        }
        // Recebe imagem e grava na variavel do tipo File
        File img1 = new File(caminho);

        try {
            // Lê imagem e armazena na variavel Buferred
            buff1 = ImageIO.read(img1);
        } catch (IOException ex) {
            Logger.getLogger("Erro: " + ex);
        }
    }//GEN-LAST:event_btnBuscar1MouseClicked

